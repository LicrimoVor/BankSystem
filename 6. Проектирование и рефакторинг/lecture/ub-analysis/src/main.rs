/// Вычисляет сумму элементов массива через сырой указатель.
///
/// # Safety
///
/// Вызывающий код должен гарантировать:
/// - `ptr` не является null
/// - `ptr` указывает на валидные данные типа `i32`
/// - Данные, на которые указывает `ptr`, содержат как минимум `len` элементов
/// - Данные, на которые указывает `ptr`, не будут освобождены до завершения функции
/// - Данные, на которые указывает `ptr`, не будут изменены другим потоком во время выполнения функции (если используется в многопоточном контексте)
unsafe fn unsafe_function(ptr: *const i32, len: usize) -> i32 {
    let mut sum = 0;
    for i in 0..len {
        sum += *ptr.add(i);
    }
    sum
}

fn safe_sum(data: &[i32]) -> i32 {
    unsafe { unsafe_function(data.as_ptr(), data.len()) }
}

fn main() {
    let data = vec![1, 2, 3, 4, 5];
    let result = safe_sum(&data);
    println!("Sum: {}", result);
}
